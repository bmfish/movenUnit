

package wk.cityhome.dowork.bean;

public class BedBean {
    private String  hotelName;
    private String hotelNum;
    private String roommodelName;
    private String roommodelType;
    private String bedName;
    private  String width;
    private  String count;

    public String getHotelName() {
        return hotelName;
    }

    public void setHotelName(String hotelName) {
        this.hotelName = hotelName;
    }

    public String getHotelNum() {
        return hotelNum;
    }

    public void setHotelNum(String hotelNum) {
        this.hotelNum = hotelNum;
    }

    public String getRoommodelName() {
        return roommodelName;
    }

    public void setRoommodelName(String roommodelName) {
        this.roommodelName = roommodelName;
    }

    public String getRoommodelType() {
        return roommodelType;
    }

    public void setRoommodelType(String roommodelType) {
        this.roommodelType = roommodelType;
    }

    public String getBedName() {
        return bedName;
    }

    public void setBedName(String bedName) {
        this.bedName = bedName;
    }

    public String getWidth() {
        return width;
    }

    public void setWidth(String width) {
        this.width = width;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }
}
