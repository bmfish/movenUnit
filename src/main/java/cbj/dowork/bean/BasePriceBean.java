

package cbj.dowork.bean;

public class BasePriceBean {
    private String roommodelId;
    private String hotelNumber;
    private String roommodelType;
    private String price;
    private String count;
    private String time;

    public String getRoommodelId() {
        return roommodelId;
    }

    public void setRoommodelId(String roommodelId) {
        this.roommodelId = roommodelId;
    }

    public String getHotelNumber() {
        return hotelNumber;
    }

    public void setHotelNumber(String hotelNumber) {
        this.hotelNumber = hotelNumber;
    }

    public String getRoommodelType() {
        return roommodelType;
    }

    public void setRoommodelType(String roommodelType) {
        this.roommodelType = roommodelType;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
